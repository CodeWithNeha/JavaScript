// Primitive DataTypes
// 1. String
let name = "Neha"
console.log("String is "+name);
console.log("Datatype is "+(typeof name));

// 2. Numbers
let marks = 88
console.log("Datatype is "+(typeof marks))

// 3. Boolean
let isTrue = true
console.log("Datatype is "+(typeof isTrue))

// 4. Null
let nullVar = null
console.log("Datatype is "+(typeof nullVar))

// 5. undefind
let unDef = undefined;
console.log("Datatype is "+(typeof unDef))

//Reference Data Type

// 1. Array
let myArr = [1,2,3,'neha']
console.log("Datatype is "+(typeof myArr))

// 2. Object Literals
let stMarks ={
    neha:89,
    heena:67,
    saqib:90
}
console.log("Datatype is "+(typeof stMarks))

